function loadRank(type) {
  const typeElement = document.querySelector(`#${type} tbody`);
  const typeList = listTop.filter((e) => e.category === `${type}`);
  typeList.forEach((e, index) => {
    const trString = `
    <th scope="row">${index + 1}</th>
        <td>
            <img
              class="thumbnail"
              src="${e.cover}"
              alt="${index + 1}"
            />
        </td>
        <td>${e.name}</td>
        <td>${e.artist}</td>
    `;
    const trElem = document.createElement("tr");
    trElem.innerHTML = trString;
    typeElement.insertAdjacentElement("beforeend", trElem);
  });
}
